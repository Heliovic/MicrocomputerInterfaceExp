PORT_0832 EQU 0600H
PORT_0809 EQU 06C0H

CYCLE EQU 02FFH

CODE SEGMENT
	ASSUME CS:CODE
START:
	
	MOV AL, 0FFH
AA1:
	MOV DX, PORT_0832
	INC AL
	OUT DX, AL
	MOV CX, CYCLE
	LOOP $
	
	CMP AL, 0F0H
	JNZ AA1
	
	DEC AL
	
AA2:
	MOV DX, PORT_0832
	DEC AL
	OUT DX, AL
	MOV CX, CYCLE
	LOOP $
	
	CMP AL, 00H
	JNZ AA2
	JMP AA1
	
CODE ENDS
	END START