PORT_0832 EQU 0600H
PORT_0809 EQU 06C0H

CYCLE EQU 04FFFH
STEP EQU 10H

CODE SEGMENT
	ASSUME CS:CODE
START:
	
	MOV AL, 00H
AA1:
	MOV DX, PORT_0832
	ADD AL, STEP
	OUT DX, AL
	MOV CX, CYCLE
	LOOP $
	MOV CX, CYCLE
	LOOP $
	MOV CX, CYCLE
	LOOP $
	MOV CX, CYCLE
	LOOP $
	
	CMP AL, 0EFH
	JNA AA1
	
AA2:
	MOV DX, PORT_0832
	SUB AL, STEP
	OUT DX, AL
	MOV CX, CYCLE
	LOOP $
	MOV CX, CYCLE
	LOOP $
	MOV CX, CYCLE
	LOOP $
	MOV CX, CYCLE
	LOOP $
	
	CMP AL, 10H
	JNB AA2
	JMP AA1
	
CODE ENDS
	END START