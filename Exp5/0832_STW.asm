PORT_0832 EQU 0600H
PORT_0809 EQU 06C0H

CYCLE EQU 05FFH

CODE SEGMENT
	ASSUME CS:CODE
START:
	
	MOV AL, 0FFH
AA1:
	MOV DX, PORT_0832
	INC AL
	OUT DX, AL
	
	;MOV DX, PORT_0809
	;OUT DX, AL
	;MOV CX, 0FFH
	;LOOP $
	;IN AL, DX
	MOV CX, CYCLE
	LOOP $
	CMP AL, 0F0H	;示波器最大貌似不是0FFH
	JNZ AA1
	MOV AL, 0
	JMP AA1
CODE ENDS
	END START