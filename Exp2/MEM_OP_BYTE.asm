DATA SEGMENT
	;数据段默认起始地址为0200H
	DATA1 DW 16 DUP(1H)
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:
	;使用如下命令设置段地址
	MOV AX, 8000H
	MOV DS, AX
	
	MOV AX, 0
	MOV CX, 16
	MOV BX, 0
	
LOP:
    ;按字节传送
	MOV BYTE PTR [BX], AL
	INC BX
	MOV BYTE PTR [BX], AH
	INC BX
	INC AX
	LOOP LOP
	
	MOV AH, 4CH
	INT 21H
CODE ENDS
END START